def get_warmup() -> list[Exercise]:
    return list(WARMUP)


def get_workout(workout_type: str) -> list[Exercise]:
    return list(WORKOUTS[workout_type.upper()])


def get_all_exercises() -> list[Exercise]:
    exercises = list(WARMUP)
    for workout in WORKOUTS.values():
        exercises.extend(workout)
    return exercises


def get_exercise_library_rows() -> list[dict]:
    rows = []
    for exercise in get_all_exercises():
        rows.append(
            {
                "Exercise ID": exercise.exercise_id,
                "Exercise Name": exercise.name,
                "Workout Type": exercise.workout_type,
                "Block": exercise.block,
                "Order": exercise.order,
                "Planned Sets": exercise.planned_sets,
                "Planned Reps": exercise.planned_reps,
                "Rest Seconds": exercise.rest_seconds,
                "Estimated Duration Minutes": exercise.estimated_minutes,
                "Technique Tips": "\n".join(exercise.technique_tips),
                "Input Example": exercise.input_example,
            }
        )
    return rows
